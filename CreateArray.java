public class CreateArray {

    public static void main(String[] args)
    {
        String rainbow[] = {"Red", "Orange", "Yellow", "Green", "Cyan", "Blue", "Violet"};

        for (int i = rainbow.length - 1; i >=0; i--)
            System.out.println(rainbow[i]);
    }
}
